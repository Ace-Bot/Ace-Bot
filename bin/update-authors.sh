#!/bin/sh

# Updates AUTHORS.md based on Git history.
# Forked from feross -> http://github.com/standard/standard/blob/a5994268773a1c12d9e1e23c786ed8f7e90d41e5/bin/update-authors.sh#L4-L24

git log --reverse --format='**%aN** (%aE)' | perl -we '
BEGIN {
  %seen = (), @authors = ();
}
while (<>) {
  next if $seen{$_};
  $seen{$_} = push @authors, "- ", $_;
}
END {
  print "# Project Authors\n\n";
  print "#### Ordered by first contribution.\n\n";
  print @authors, "\n";
  print "#### Generated by `bin/update-authors.sh`. ðŸš€\n";
}
' > AUTHORS.md
